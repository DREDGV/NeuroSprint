/**
 * Получить подсказку для типа паттерна
 */
export function getPatternHint(patternType: PatternType): PatternHint {
  return PATTERN_HINTS[patternType] || PATTERN_HINTS.ABAB;
}

/**
 * Получить краткую подсказку для отображения в вопросе
 */
export function getPatternShortHint(patternType: PatternType): string {
  const hint = PATTERN_HINTS[patternType];
  return hint ? hint.rule : "Найдите закономерность";
}
